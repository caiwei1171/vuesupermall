<template>
    <div class="tab-control">
        <div v-for="(item,index) in titles" v-bind:key="index" class="tab-control-item"
         v-bind:class="{active:index === currentIndex}" @click="itemClick(index)">
            <span>{{item}}</span>
        </div>
    </div>
</template>

<script>
export default {
    name:"TabControl",
    props:{
        titles:{
            type:Array,
            default(){
                return []
            }
        }
    },
    data(){
        return {
            currentIndex:0//用来记录是否处于选择状态
        }
    },
    methods:{
        itemClick(index){
            this.currentIndex = index;
            this.$emit("tabClick",index);
        }
    }
}
</script>

